void setup() {

  SPI.begin();
  
  if(!display.begin(SSD1306_SWITCHCAPVCC, 0x3c)) { // Address 0x3C for 128x64
    Serial.println(F("SSD1306 allocation failed"));
    for(;;); // Don't proceed, loop forever
  }

}

void loop() {

    numFrame++;
    if (numFrame >= 12) { // if the animation ends
      numFrame = 0; // start again from the first frame
    }

    display.clearDisplay(); // clear display buffer
    display.drawBitmap(0, 16, nyancatframes[numFrame], 128, 32, WHITE); // draw current frame
    display.display(); // refresh the display to show changes


  delay(20);

}
